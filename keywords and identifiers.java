// Example of Keywords and Identifiers
public class Test {   // 'public' and 'class' are keywords, 'Test' is identifier
    public static void main(String[] args) {  // 'static', 'void' are keywords, 'main' is identifier
        int age = 20;   // 'int' is keyword, 'age' is identifier
        String name = "Ali"; // 'String' is class (not keyword), 'name' is identifier

        System.out.println("Name: " + name + ", Age: " + age);
    }
}